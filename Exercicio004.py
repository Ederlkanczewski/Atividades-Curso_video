# Exercício Python 4: 
# Faça um programa que leia algo pelo teclado e mostre na tela 
# o seu tipo primitivo e todas as informações possíveis sobre ele.
a = input('digite algo : ')
print(f'O tipo primitivo desse valor é ',type(a))
print(f'O tipo primitivo desse valor é ',a.isalnum())
print(f'O tipo primitivo desse valor é ',a.isalpha())
print(f'O tipo primitivo desse valor é ',a.isascii())
print(f'O tipo primitivo desse valor é ',a.isdecimal())
print(f'O tipo primitivo desse valor é ',a.isdigit())
print(f'O tipo primitivo desse valor é ',a.isidentifier())
print(f'O tipo primitivo desse valor é ',a.islower()),
print(f'O tipo primitivo desse valor é ',a.isnumeric())
print(f'O tipo primitivo desse valor é ',a.isprintable())
print(f'O tipo primitivo desse valor é ',a.isspace())
print(f'O tipo primitivo desse valor é ',a.istitle())
print(f'O tipo primitivo desse valor é ',a.isupper())